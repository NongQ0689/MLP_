import numpy as np

# Define the activation function (sigmoid)
def sigmoid(x):
    return 1 / (1 + np.exp(-x))

# Define the derivative of the sigmoid function
def sigmoid_derivative(x):
    return sigmoid(x) * (1 - sigmoid(x))

# Define the MLP class
class MLP:
    def __init__(self, input_size, hidden_size, output_size):
        self.input_size = input_size
        self.hidden_size = hidden_size
        self.output_size = output_size
        
        # Initialize the weights with random values
        self.weights_input_hidden = np.random.randn(self.input_size, self.hidden_size)
        self.weights_hidden_output = np.random.randn(self.hidden_size, self.output_size)
        
        # Initialize the biases with zeros
        self.bias_hidden = np.zeros((1, self.hidden_size))
        self.bias_output = np.zeros((1, self.output_size))
        
    def forward(self, X):
        # Forward pass through the network
        self.hidden_layer_output = sigmoid(np.dot(X, self.weights_input_hidden) + self.bias_hidden)
        self.output = sigmoid(np.dot(self.hidden_layer_output, self.weights_hidden_output) + self.bias_output)
        
    def backward(self, X, Y, learning_rate):
        # Backward pass through the network
        error = Y - self.output

        # Calculate gradients for weights and biases
        delta_output = error * sigmoid_derivative(self.output)
        delta_hidden = np.dot(delta_output, self.weights_hidden_output.T) * sigmoid_derivative(self.hidden_layer_output)
        
        gradient_weights_hidden_output = np.dot(self.hidden_layer_output.T, delta_output)
        gradient_weights_input_hidden = np.dot(X.T, delta_hidden)
        
        gradient_bias_hidden = np.sum(delta_hidden, axis=0)
        gradient_bias_output = np.sum(delta_output, axis=0)
        
        # Update weights and biases
        self.weights_hidden_output += learning_rate * gradient_weights_hidden_output
        self.weights_input_hidden += learning_rate * gradient_weights_input_hidden
        
        self.bias_hidden += learning_rate * gradient_bias_hidden
        self.bias_output += learning_rate * gradient_bias_output
        
    def train(self, X, y, epochs, learning_rate):
        for epoch in range(epochs):
            self.forward(X)
            self.backward(X, Y, learning_rate)
            if (epoch+1) % 2000 == 0 : print( str(np.around((epoch+1)/epochs*100,decimals = 2)) +" %")
            
        
    def predict(self, X):
        self.forward(X)
        return self.output

def output_dec(output) :
    sum = 0
    for i in range(len(output)):
        output[i] = 1 if output[i] > 0.5 else 0
        sum += (2**(9-i))*output[i]
    return sum

def output_bin(output) :
    for i in range(len(output)):
        output[i] = 1 if output[i] > 0.5 else 0
    return output


